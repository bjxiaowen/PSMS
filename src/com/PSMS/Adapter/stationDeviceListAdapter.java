package com.PSMS.Adapter;

public class stationDeviceListAdapter {
	private int period_id;
	private String period;
	private String inverter_name;
	private String jb_name;
	private String pm_name;
	private String ws_name;
	public int getPeriod_id() {
		return period_id;
	}
	public void setPeriod_id(int period_id) {
		this.period_id = period_id;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getInverter_name() {
		return inverter_name;
	}
	public void setInverter_name(String inverter_name) {
		this.inverter_name = inverter_name;
	}
	public String getJb_name() {
		return jb_name;
	}
	public void setJb_name(String jb_name) {
		this.jb_name = jb_name;
	}
	public String getPm_name() {
		return pm_name;
	}
	public void setPm_name(String pm_name) {
		this.pm_name = pm_name;
	}
	public String getWs_name() {
		return ws_name;
	}
	public void setWs_name(String ws_name) {
		this.ws_name = ws_name;
	}
	
	

}
