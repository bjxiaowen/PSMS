package com.PSMS.Adapter;

public class powerhistory_month {
	
	private int id;
	private String ps_name;
    private double total_power;
    private int total_hour;
    private int year;
    private int month;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPs_name() {
		return ps_name;
	}
	public void setPs_name(String ps_name) {
		this.ps_name = ps_name;
	}
	public double getTotal_power() {
		return total_power;
	}
	public void setTotal_power(double total_power) {
		this.total_power = total_power;
	}
	public int getTotal_hour() {
		return total_hour;
	}
	public void setTotal_hour(int total_hour) {
		this.total_hour = total_hour;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
    
    

}
