package com.PSMS.Adapter;

public class junctionbox_history {
	private int id;
	private String junctionbox_name;
	private int pv_unit;
	private Double pv_current;
	private Double pv_voltage;
	private String time;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJunctionbox_name() {
		return junctionbox_name;
	}
	public void setJunctionbox_name(String junctionbox_name) {
		this.junctionbox_name = junctionbox_name;
	}
	public int getPv_unit() {
		return pv_unit;
	}
	public void setPv_unit(int pv_unit) {
		this.pv_unit = pv_unit;
	}
	public Double getPv_current() {
		return pv_current;
	}
	public void setPv_current(Double pv_current) {
		this.pv_current = pv_current;
	}
	public Double getpv_voltage() {
		return pv_voltage;
	}
	public void setPv_voltage(Double pv_voltage) {
		this.pv_voltage = pv_voltage;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
