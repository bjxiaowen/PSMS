package com.PSMS.Adapter;

public class powermeter_history {
	private int id;
	private String powermeter_name;
	private Double acc_power;
	private Double active_power;
	private Double reactive_power;
	private Double power_factor;
	private Double ac_current;
	private Double ac_voltage;
	private Double ac_power;
	private String time;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPowermeter_name() {
		return powermeter_name;
	}
	public void setPowermeter_name(String powermeter_name) {
		this.powermeter_name = powermeter_name;
	}
	public Double getAcc_power() {
		return acc_power;
	}
	public void setAcc_power(Double acc_power) {
		this.acc_power = acc_power;
	}
	public Double getActive_power() {
		return active_power;
	}
	public void setActive_power(Double active_power) {
		this.active_power = active_power;
	}
	public Double getReactive_power() {
		return reactive_power;
	}
	public void setReactive_power(Double reactive_power) {
		this.reactive_power = reactive_power;
	}
	public Double getPower_factor() {
		return power_factor;
	}
	public void setPower_factor(Double power_factor) {
		this.power_factor = power_factor;
	}
	public Double getAc_current() {
		return ac_current;
	}
	public void setAc_current(Double ac_current) {
		this.ac_current = ac_current;
	}
	public Double getAc_voltage() {
		return ac_voltage;
	}
	public void setAc_voltage(Double ac_voltage) {
		this.ac_voltage = ac_voltage;
	}
	public Double getAc_power() {
		return ac_power;
	}
	public void setAc_power(Double ac_power) {
		this.ac_power = ac_power;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
